package com.model;

/**
 * 类型信息Model类
 */
public class Zptype {
    public Zptype() {
    }

    /**
     * id
     */
    private Integer id;
    /**
     * 类型名称
     */
    private String name;

    public Integer getId() {
        return this.id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
